package model;

import java.util.Date;

/**
 *
 * @author DELL
 */
public class Ticket {
    private String id;
    private Date date;
    private Boolean status;

    public Ticket(String id, Date date, Boolean status) {
        this.id = id;
        this.date = date;
        this.status = status;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public Date getDate() {
        return date;
    }

    public void setDate(Date date) {
        this.date = date;
    }

    public Boolean getStatus() {
        return status;
    }

    public void setStatus(Boolean status) {
        this.status = status;
    }
    
    
}
